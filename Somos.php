             <h2>Quienes Somos </h2>
             <p>
               El Instituto Psicopedagógico Juana Leclerc, es una Institución privada sin fines de lucro, fundada en el año 1973, con personería Jurídica Nº 112/19/10/78, dedicada a la educación integral de las personas con discapacidad, a través de la  promoción de oportunidades para su inclusión en el marco de los derechos humanos.
             </p>
             <p>
               Su política de admisión y de atención integral es de puertas abiertas a todo niño, niña y joven que se encuentren en situación de discapacidad, sin distinción de edad, credo, sexo o estrato social.
             </p>
             <p>
               Durante sus 42 años de trayectoria, el Instituto Juana Leclerc ha evolucionado de acuerdo a las nuevas tendencias y estrategias de atención del campo de la rehabilitación y educación especial; así mismo, se ha preocupado por la capacitación de su personal, padres de familia y voluntarios dentro y fuera del país; lo cual le ha permitido mantenerse a la vanguardia de las nuevas tendencias en el campo de la discapacidad y  temáticas afines, ejerciendo un reconocido liderazgo en el área de la rehabilitación, integración e inclusión de la persona con discapacidad que ha favorecido su reconocimiento nacional e  internacional, como una institución confiable que ofrece servicios de calidad.
             </p>
             <h2>Misión</h2>
             <p>
               Somos una institución sin fines de lucro dedicada a la educación integral de las personas con discapacidad, promoviendo oportunidades para su inclusión en el marco de los Derechos Humanos.
             </p>
             <h2>Visión</h2>
             <p>
               Al 2017, ser referente idóneo en la gestión del conocimiento, atención, promoción, formación en el campo de la discapacidad para favorecer la plena inclusión de las PCD.
             </p>
             <h2>Filosofía</h2>
             <p>
               Se sustenta en el principio que todo ser humano tiene igual derecho y oportunidad para promover su propio desarrollo dentro de la sociedad, y de que toda niña y niño pueda aprendeer, crecer y madurar siempre que se le brinde la oportunidad.
